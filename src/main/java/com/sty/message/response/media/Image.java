package com.sty.message.response.media;

public class Image {
	
	private String MediaId;
	
	public Image() {
		super();
	}

	public Image(String mediaId) {
		super();
		MediaId = mediaId;
	}

	public String getMediaId() {
		return MediaId;
	}

	public void setMediaId(String mediaId) {
		MediaId = mediaId;
	}
	
}
